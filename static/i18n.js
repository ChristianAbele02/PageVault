  window.PV_LANG = (stored === 'de' || stored === 'en') ? stored : 'en';

  /**
   * Translate a key in the current language.
   * Optional `params` object replaces `{placeholder}` tokens in the string.
   */
  window.t = function (key, params) {
    const dict = T[window.PV_LANG] || T.en;
    let text = dict[key] !== undefined ? dict[key] : (T.en[key] !== undefined ? T.en[key] : key);
    if (params) {
      for (const [name, value] of Object.entries(params)) {
        text = text.split(`{${name}}`).join(String(value));
      }
    }
    return text;
  };

  window.applyLang = function (lang) {
    window.PV_LANG = lang;
    localStorage.setItem('pv_lang', lang);
    const dict = T[lang] || T.en;

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      if (dict[key] !== undefined) el.textContent = dict[key];
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.dataset.i18nPlaceholder;
      if (dict[key] !== undefined) el.placeholder = dict[key];
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      const key = el.dataset.i18nTitle;
      if (dict[key] !== undefined) el.title = dict[key];
    });
    document.querySelectorAll('[data-lang-toggle]').forEach(btn => {
      btn.textContent = lang === 'en' ? 'DE' : 'EN';
    });
    document.documentElement.lang = lang;
  };

  window.toggleLang = function () {
    window.applyLang(window.PV_LANG === 'en' ? 'de' : 'en');
  };
